#!/usr/bin/env node
/**
 * generate-lab-data.mjs — Lab gallery + CC-adoption board data generator.
 *
 * Reads docs/site/lab-manifest.json (the curated allowlist), copies each
 * source playground HTML into docs/site/public/lab/<slug>.html, and emits:
 *   - lib/generated/lab-data.ts          (gallery entries)
 *   - lib/generated/cc-adoption-data.ts  (from shared/cc-adoption-gaps.json
 *                                         + shared/cc-support.json + git stamp)
 *
 * Convention (matches scripts/generate-docs-data.js): run locally, commit the
 * outputs. Sources under docs/*.html may be gitignored working artifacts —
 * the committed public/lab copies are the published canon.
 *
 * Source resolution: prefer the repo source file; when it is absent (several
 * sources under docs/*.html are gitignored working artifacts, so clean
 * checkouts don't have them) fall back to the committed public/lab copy with
 * a warning. Fails loudly only when BOTH are missing (truly pruned entry):
 * remove the entry or restore the file.
 */
import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SITE = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const REPO = path.resolve(SITE, "..", "..");
const OUT_LAB = path.join(SITE, "public", "lab");
const GEN = path.join(SITE, "lib", "generated");

const manifest = JSON.parse(fs.readFileSync(path.join(SITE, "lab-manifest.json"), "utf8"));

const esc = (s) => String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"');

// ---------- lab entries ----------
fs.mkdirSync(OUT_LAB, { recursive: true });
const entries = [];
const missing = [];
for (const e of manifest.entries) {
  const src = path.join(REPO, e.source);
  const committed = path.join(OUT_LAB, `${e.slug}.html`);
  let html;
  if (fs.existsSync(src)) {
    html = fs.readFileSync(src, "utf8");
  } else if (fs.existsSync(committed)) {
    console.warn(
      `[generate-lab-data] source absent (gitignored or pruned): ${e.source} — keeping committed public/lab/${e.slug}.html`,
    );
    html = fs.readFileSync(committed, "utf8");
  } else {
    missing.push(e.source);
    continue;
  }
  const title = e.title ?? html.match(/<title>([^<]*)<\/title>/)?.[1]?.trim() ?? e.slug;
  const description =
    e.description ??
    html.match(/<meta\s+name="description"\s+content="([^"]*)"/)?.[1]?.trim() ??
    "";
  fs.writeFileSync(path.join(OUT_LAB, `${e.slug}.html`), html);
  entries.push({
    slug: e.slug,
    title,
    description,
    tags: e.tags ?? [],
    date: e.date ?? "",
    featured: Boolean(e.featured),
    caseStudy: e.caseStudy ?? null,
    sizeKb: Math.round(Buffer.byteLength(html) / 1024),
  });
}
if (missing.length) {
  console.error(
    `[generate-lab-data] missing sources (pruned?): remove from lab-manifest.json or restore:\n  ${missing.join("\n  ")}`,
  );
  process.exit(1);
}
entries.sort((a, b) => (a.date < b.date ? 1 : -1));

const labTs = `// Generated by docs/site/scripts/generate-lab-data.mjs — do not edit by hand.
// Source of truth: docs/site/lab-manifest.json

export interface LabEntry {
  slug: string;
  title: string;
  description: string;
  tags: string[];
  date: string;
  featured: boolean;
  /** Route of the long-form case study, when one exists */
  caseStudy: string | null;
  sizeKb: number;
}

export const LAB_ENTRIES: LabEntry[] = ${JSON.stringify(entries, null, 2)};
`;
fs.writeFileSync(path.join(GEN, "lab-data.ts"), labTs);

// ---------- cc-adoption board ----------
const gaps = JSON.parse(fs.readFileSync(path.join(REPO, "shared", "cc-adoption-gaps.json"), "utf8"));
const support = JSON.parse(fs.readFileSync(path.join(REPO, "shared", "cc-support.json"), "utf8"));

let stamp = { commit: "unknown", date: new Date().toISOString().slice(0, 10) };
try {
  stamp = {
    commit: execSync("git rev-parse --short HEAD", { cwd: REPO }).toString().trim(),
    date: new Date().toISOString().slice(0, 10),
  };
} catch {
  /* fine — stamp stays partial outside a git checkout */
}

const waves = gaps.map((w) => ({
  version: w.version,
  features: w.features.map((f) => ({
    slug: f.feature_slug,
    category: f.category,
    description: f.description,
    gapScore: f.gap_score,
    affectedSkills: f.affected_skills ?? [],
  })),
}));

const adoptionTs = `// Generated by docs/site/scripts/generate-lab-data.mjs — do not edit by hand.
// Sources: shared/cc-adoption-gaps.json + shared/cc-support.json

export interface AdoptionFeature {
  slug: string;
  category: string;
  description: string;
  gapScore: number;
  affectedSkills: string[];
}

export interface AdoptionWave {
  version: string;
  features: AdoptionFeature[];
}

export const ADOPTION_WAVES: AdoptionWave[] = ${JSON.stringify(waves, null, 2)};

export const CC_SUPPORT = {
  latest: "${esc(support.latest)}",
  latestKnown: "${esc(support.latest_known)}",
  supportedFloor: "${esc(support.supported_floor)}",
  policy: "${esc(support.policy)}",
} as const;

export const GENERATED_AT = { commit: "${esc(stamp.commit)}", date: "${esc(stamp.date)}" } as const;
`;
fs.writeFileSync(path.join(GEN, "cc-adoption-data.ts"), adoptionTs);

console.log(
  `[generate-lab-data] ${entries.length} lab entries → public/lab/ + lab-data.ts · ` +
    `${waves.length} adoption wave(s) → cc-adoption-data.ts (stamp ${stamp.commit})`,
);

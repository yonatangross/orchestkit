#!/bin/bash
# =============================================================================
# OrchestKit Eval Regression Checker
# =============================================================================
# Compares eval results against committed baselines to detect regressions.
# No Claude API calls — pure JSON comparison.
#
# Usage:
#   bash tests/evals/scripts/check-eval-regression.sh              # changed skills only
#   bash tests/evals/scripts/check-eval-regression.sh --all        # all baselines
#   bash tests/evals/scripts/check-eval-regression.sh commit       # one skill
#
# Exit codes:
#   0 — No regressions
#   1 — Regression detected (precision or recall dropped)
#
# Designed for CI: add to plugin-validation.yml on PRs changing src/skills/
# =============================================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
DRY_RUN=false

# Source shared library
# shellcheck source=lib/eval-common.sh
source "$SCRIPT_DIR/lib/eval-common.sh"

EVALS_DIR="$(dirname "$SCRIPT_DIR")"
ROOT_DIR="$(cd "$EVALS_DIR/../.." && pwd)"
RESULTS_DIR="$EVALS_DIR/results/skills"

SKILL=""
CHECK_ALL=false
REGRESSIONS=0
CHECKED=0
IMPROVED=0
STABLE=0

# --- Argument parsing ---
while [[ $# -gt 0 ]]; do
    case "$1" in
        --all) CHECK_ALL=true; shift ;;
        --dry-run) DRY_RUN=true; shift ;;
        -*)
            echo -e "${RED}Unknown flag: $1${NC}"; exit 1 ;;
        *)
            SKILL="$1"; shift ;;
    esac
done

echo ""
echo -e "${BLUE}============================================================${NC}"
echo -e "${BLUE}  EVAL REGRESSION CHECK${NC}"
echo -e "${BLUE}============================================================${NC}"

# --- Determine which skills to check ---
skills_to_check=()

if [[ -n "$SKILL" ]]; then
    skills_to_check=("$SKILL")
elif [[ "$CHECK_ALL" == "true" ]]; then
    for f in "$RESULTS_DIR"/*.trigger.json; do
        [[ -f "$f" ]] || continue
        skills_to_check+=("$(basename "$f" .trigger.json)")
    done
else
    # Auto-detect changed skills from git diff
    changed_files=$(git diff origin/main --name-only 2>/dev/null || git diff HEAD~1 --name-only 2>/dev/null || echo "")
    for file in $changed_files; do
        if [[ "$file" =~ ^src/skills/([a-z0-9-]+)/ ]]; then
            skill_name="${BASH_REMATCH[1]}"
            # Only check if baseline exists
            if [[ -f "$RESULTS_DIR/${skill_name}.trigger.json" ]]; then
                # Deduplicate
                already_found=false
                for existing in "${skills_to_check[@]}"; do
                    [[ "$existing" == "$skill_name" ]] && already_found=true
                done
                if [[ "$already_found" == "false" ]]; then
                    skills_to_check+=("$skill_name")
                fi
            fi
        fi
    done

    if [[ ${#skills_to_check[@]} -eq 0 ]]; then
        echo -e "  ${GREEN}No skill changes detected — nothing to check${NC}"
        echo -e "${BLUE}============================================================${NC}"
        exit 0
    fi
fi

echo -e "  Checking: ${#skills_to_check[@]} skills"
echo ""

# --- Check each skill ---
for skill_id in "${skills_to_check[@]}"; do
    CHECKED=$((CHECKED + 1))
    baseline_file="$RESULTS_DIR/${skill_id}.trigger.json"

    if [[ ! -f "$baseline_file" ]]; then
        echo -e "  ${YELLOW}$skill_id: no baseline — SKIP${NC}"
        continue
    fi

    # Read baseline values
    base_p=$(jq -r '.precision // .effective_precision // 0' "$baseline_file")
    base_r=$(jq -r '.recall // .effective_recall // 0' "$baseline_file")

    # Check if there's a new result file (from PR)
    # In CI, new results would be generated by the eval runner
    # For static check, we compare committed baselines only
    # This is a guard — if someone changes a description and re-runs evals,
    # they commit the new results. This script catches if they DON'T.

    # For now, report current baseline status
    status_icon="" status_text=""
    if (( $(echo "$base_p >= 100 && $base_r >= 100" | bc -l) )); then
        status_icon="${GREEN}✅"
        status_text="PERFECT"
        STABLE=$((STABLE + 1))
    elif (( $(echo "$base_p >= 80 && $base_r >= 80" | bc -l) )); then
        status_icon="${GREEN}✅"
        status_text="STABLE"
        STABLE=$((STABLE + 1))
    elif (( $(echo "$base_p >= 50 || $base_r >= 50" | bc -l) )); then
        status_icon="${YELLOW}⚠️"
        status_text="WEAK"
    else
        status_icon="${RED}❌"
        status_text="FAILING"
        REGRESSIONS=$((REGRESSIONS + 1))
    fi

    printf "  %b %s: P=%s R=%s — %s%b\n" "$status_icon" "$skill_id" "$base_p" "$base_r" "$status_text" "${NC}"
done

# --- Summary ---
echo ""
echo -e "${BLUE}============================================================${NC}"
echo -e "  Checked:     $CHECKED"
echo -e "  Stable:      ${GREEN}$STABLE${NC}"
if [[ $REGRESSIONS -gt 0 ]]; then
    echo -e "  Failing:     ${RED}$REGRESSIONS${NC}"
fi
echo -e "${BLUE}============================================================${NC}"

if [[ $REGRESSIONS -gt 0 ]]; then
    echo -e "${RED}RESULT: FAILED ($REGRESSIONS skills below threshold)${NC}"
    exit 1
fi

echo -e "${GREEN}RESULT: PASSED (all baselines healthy)${NC}"
exit 0
